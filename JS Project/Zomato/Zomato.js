document.getElementById('apply-btn').addEventListener('click', ()=>{
     const result= document.querySelector('input[name="sortBy"]:checked');


    let arr;
    if(result.value === "rating"){
      arr= [...restaurant].sort((a,b)=> b.cardRating-a.cardRating);
    } 
    else if(result.value === "costLowHigh"){
      arr= [...restaurant].sort((a,b)=> a.cardFoodPrice-b.cardFoodPrice); 
    } 
    else if(result.value === "costHighLow"){
      arr= [...restaurant].sort((a,b)=> b.cardFoodPrice-a.cardFoodPrice);
    } 
    else if(result.value === "distance"){
      arr= [...restaurant].sort((a,b)=> a.cardResDist-b.cardResDist);
    } 


    document.getElementById('filter').style.backgroundColor= "crimson";
    document.getElementById('filter').style.color= "white";

    document.getElementById('root').replaceChildren();
    getRestaurant(arr);

    document.getElementById('filterPOP').style.display= "none";
    document.body.style.overflow= "auto";
});


