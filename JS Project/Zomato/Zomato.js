function getRestaurant(restaurant){

  restaurant.forEach((eachRes)=>{


    //main -> Section(#root)
    //          ->div(.card1)
    //                ->did(.pic)
        //                ->img(.img)
        //                ->p(.off)

    //                ->div(.detail) 
        //                ->div(.rstname) 
        //                      ->p 
        //                ->div(.rating) 
        //                      ->p 
        
        //                ->div(.foodtype) 
        //                ->div(.cuisine) 
        //                      ->p 
        //                ->div(.price) 
        //                      ->p 
        
        //                ->div(.courier) 
        //                ->div(.order) 
        //                      ->p 
        //                ->div(.dist) 
        //                      ->p 



    // Section
    const root= document.getElementById('root');

    //Card1
    const CARD1= document.createElement('div'); 
    CARD1.classList.add('card1');

    CARD1.innerHTML= `
                  <div class="pic"> 
                    <img src="./Media/${eachRes.cardImage}.png" alt="card1" class="img">
                    <p class="off">&nbsp;Flat ${eachRes.cardOffer}% OFF&nbsp;&nbsp;</p>
                </div>


                <div class="detail">
                    <div class="rstname">
                        <p>${eachRes.cardResName}</p>
                    </div>
                    <div class="rating">
                        <p>${eachRes.cardRating}&starf;</p>
                    </div>
                </div>

                <div class="foodtype">
                    <div class="cuisine">
                        <p>${eachRes.cardFoodType}</p>
                    </div>
                    <div class="price">
                        <p>&#8377;${eachRes.cardFoodPrice} for Two</p>
                    </div>
                </div>


                <div class="courier">
                    <div class="order">
                        <p>${eachRes.cardResLoc}</p>
                    </div>
                    <div class="dist">
                        <p>${eachRes.cardResDist} km</p>
                    </div>
                </div>
    `;

    root.appendChild(CARD1);
  })
}

